namespace MMSINC.Helpers
{
    public enum ChartLegendPosition
    {
        /// <summary>
        /// Default value used by AmCharts. Legend displays underneath chart.
        /// </summary>
        Bottom = 0,

        /// <summary>
        /// Legend displays to the right of the chart.
        /// </summary>
        Right = 1,

        /// <summary>
        /// No legend is displayed.
        /// </summary>
        None = 2
    }
}
